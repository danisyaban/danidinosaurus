<p>
Dinosaurus tertinggi yang pernah ditemukan adalah Spinosaurus aegyptiacus, meskipun beberapa spesies dinosaurus lainnya juga dikenal memiliki ukuran yang sangat besar. Spinosaurus aegyptiacus hidup sekitar 100 juta tahun yang lalu di wilayah yang sekarang menjadi Afrika Utara dan diperkirakan memiliki tinggi sekitar 15-18 meter dan berat sekitar 7-20 ton.</p>
<p>
Namun, Spinosaurus aegyptiacus tidak hanya dikenal sebagai dinosaurus tertinggi, tetapi juga salah satu dinosaurus karnivora terbesar yang pernah ada. Spinosaurus memiliki moncong yang panjang dan sempit, berguna untuk menangkap ikan di sungai dan danau. Dengan cakar tajam dan gigi yang kuat, Spinosaurus juga dapat memburu hewan lain yang hidup di sekitarnya.</p>
<p>
Beberapa spesies dinosaurus lainnya yang juga memiliki ukuran yang sangat besar dan mencakup beberapa jenis sauropoda, seperti Argentinosaurus dan Patagotitan, yang diperkirakan memiliki panjang sekitar 30-40 meter.
